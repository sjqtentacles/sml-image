#!/usr/bin/env python3
# gen_fixtures.py -- regenerate test/fixtures.sml with authoritative image
# fixtures produced OUTSIDE this repo (python zlib + manual PNG assembly). This
# validates the decoders against an independent encoder. Run:
#   python3 bin/gen_fixtures.py
import zlib, struct, os

# A small known 3x2 RGBA image (row-major, top-left origin).
W, H = 3, 2
PIX = [
    (10, 20, 30, 255),  (40, 50, 60, 255),  (70, 80, 90, 255),
    (100,110,120,200),  (130,140,150,128),  (160,170,180,64),
]

def png_chunk(typ, data):
    c = typ + data
    return struct.pack(">I", len(data)) + c + struct.pack(">I", zlib.crc32(c) & 0xffffffff)

def paeth(a, b, c):
    p = a + b - c
    pa, pb, pc = abs(p-a), abs(p-b), abs(p-c)
    if pa <= pb and pa <= pc: return a
    if pb <= pc: return b
    return c

def filter_scanlines(rows, bpp, ftype):
    """rows: list of bytes (one per scanline). Apply a single filter type to all."""
    out = bytearray()
    prev = bytes(len(rows[0]))
    for row in rows:
        out.append(ftype)
        line = bytearray()
        for i, x in enumerate(row):
            a = row[i-bpp] if i >= bpp else 0
            b = prev[i]
            c = prev[i-bpp] if i >= bpp else 0
            if ftype == 0: v = x
            elif ftype == 1: v = (x - a) & 0xff
            elif ftype == 2: v = (x - b) & 0xff
            elif ftype == 3: v = (x - ((a+b)//2)) & 0xff
            elif ftype == 4: v = (x - paeth(a,b,c)) & 0xff
            line.append(v)
        out += line
        prev = row
    return bytes(out)

def make_png(color_type, ftype):
    if color_type == 6:    # RGBA
        bpp = 4
        rows = [bytes(b for p in PIX[y*W:(y+1)*W] for b in p) for y in range(H)]
    elif color_type == 2:  # RGB
        bpp = 3
        rows = [bytes(b for p in PIX[y*W:(y+1)*W] for b in p[:3]) for y in range(H)]
    elif color_type == 0:  # gray (use red channel)
        bpp = 1
        rows = [bytes(PIX[y*W+x][0] for x in range(W)) for y in range(H)]
    elif color_type == 4:  # gray+alpha
        bpp = 2
        rows = [bytes(b for x in range(W) for b in (PIX[y*W+x][0], PIX[y*W+x][3])) for y in range(H)]
    raw = filter_scanlines(rows, bpp, ftype)
    idat = zlib.compress(raw, 9)
    ihdr = struct.pack(">IIBBBBB", W, H, 8, color_type, 0, 0, 0)
    return (b"\x89PNG\r\n\x1a\n"
            + png_chunk(b"IHDR", ihdr)
            + png_chunk(b"IDAT", idat)
            + png_chunk(b"IEND", b""))

def sml_list(bs):
    return "[" + ",".join("0wx%02X" % b for b in bs) + "]"

here = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
out = os.path.join(here, "test", "fixtures.sml")

expected = []
for (r,g,b,a) in PIX:
    expected += [r,g,b,a]
expected_opaque = []
for (r,g,b,a) in PIX:
    expected_opaque += [r,g,b,255]
expected_gray = []
for (r,g,b,a) in PIX:
    expected_gray += [r,r,r,a]
expected_gray0 = []
for (r,g,b,a) in PIX:
    expected_gray0 += [r,r,r,255]

with open(out, "w") as f:
    f.write("(* fixtures.sml -- GENERATED by bin/gen_fixtures.py. Do not edit.\n")
    f.write("   Authoritative PNG fixtures built with python's zlib; the image is a\n")
    f.write("   known %dx%d RGBA raster. *)\n\n" % (W, H))
    f.write("structure Fixtures =\nstruct\n")
    f.write("  fun bytes l = Word8Vector.fromList l\n")
    f.write("  val width = %d\n  val height = %d\n\n" % (W, H))
    f.write("  (* expected RGBA8 data (flat) for the full-color decode *)\n")
    f.write("  val expected = bytes %s\n\n" % sml_list(expected))
    f.write("  (* expected when alpha is forced opaque (RGB / grayscale-noalpha) *)\n")
    f.write("  val expected_opaque = bytes %s\n\n" % sml_list(expected_opaque))
    f.write("  (* expected grayscale expansion with original alpha (color type 4) *)\n")
    f.write("  val expected_gray = bytes %s\n\n" % sml_list(expected_gray))
    f.write("  (* expected grayscale expansion, alpha forced opaque (color type 0) *)\n")
    f.write("  val expected_gray0 = bytes %s\n\n" % sml_list(expected_gray0))
    fnames = {0:"none",1:"sub",2:"up",3:"avg",4:"paeth"}
    for ft, fn in fnames.items():
        f.write("  val png_rgba_%s = bytes %s\n" % (fn, sml_list(make_png(6, ft))))
    f.write("\n")
    f.write("  val png_rgb   = bytes %s\n" % sml_list(make_png(2, 0)))
    f.write("  val png_gray  = bytes %s\n" % sml_list(make_png(0, 1)))
    f.write("  val png_gray_alpha = bytes %s\n" % sml_list(make_png(4, 4)))
    f.write("end\n")
print("wrote", out)
